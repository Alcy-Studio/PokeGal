	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGMapRegionD:
			[self setComboNavButtonD];
			break;
		default:
			break;
	}
	
};
