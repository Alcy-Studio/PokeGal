#import "ActPGSMSBackButtonTUpInside.h"

//Categories
#import "ActPGSMSBackButtonTUpInside+Combo.h"

//Constants
#import "ConstantsAuditorUserDefaults.h"

@implementation ActPGSMSBackButtonTUpInside

-(id)initWithRole:(unsigned int)r{
	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGSMSBack:
			[self setComboBackButton];
			break;
		default:
			break;
	}
	
};
@end

