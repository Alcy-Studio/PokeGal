#import "ActPGPlaceRegionFButtonTUpInside+Combo.h"

//Constants
#import "ConstantsAuditorUserDefaults.h"

@implementation ActPGPlaceRegionFButtonTUpInside


-(id)initWithRole:(unsigned int)r{
	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGPlaceRegionF:
			[self setComboRegionButton];
			break;
		default:
			break;
	}
	
}

@end
