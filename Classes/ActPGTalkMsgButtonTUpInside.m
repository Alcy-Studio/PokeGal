#import "ActPGTalkMsgButtonTUpInside.h"

//Categories
#import "ActPGTalkMsgButtonTUpInside+Combo.h"

//Constants
#import "ConstantsAuditorUserDefaults.h"

@implementation ActPGTalkMsgButtonTUpInside

-(id)initWithRole:(unsigned int)r{
	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGTalkMsgButton:
			[self setComboMsgButton];
			break;
		default:
			break;
	}
	
};
@end

