#import "ActPGPlaceBackButtonTUpInside.h"

//Categories
#import "ActPGPlaceBackButtonTUpInside+Combo.h"

//Constants
#import "ConstantsAuditorUserDefaults.h"

@implementation ActPGPlaceBackButtonTUpInside

-(id)initWithRole:(unsigned int)r{
	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGPlaceBack:
			[self setComboBackButton];
			break;
		default:
			break;
	}
	
}
@end

