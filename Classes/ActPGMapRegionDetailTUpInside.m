#import "ActPGMapRegionDetailTUpInside.h"

//Categories
#import "ActPGMapRegionDetailTUpInside+Combo.h"

//Constants
#import "ConstantsAuditorUserDefaults.h"

@implementation ActPGMapRegionDetailTUpInside

-(id)initWithRole:(unsigned int)r{
	self = [super init];
	if(!self)
		return self;
	self.role = r;
	return self;
}
-(void)setCombo{
	switch (self.role) {
		case kAudUDNumPGMapRegionNoneDetail:
			[self setComboRegionNoneDetail];
			break;
			
		case kAudUDNumPGMapRegionADetail:
			[self setComboRegionADetail];
			break;
		case kAudUDNumPGMapRegionBDetail:
			[self setComboRegionBDetail];
			break;
		case kAudUDNumPGMapRegionCDetail:
			[self setComboRegionCDetail];
			break;
		case kAudUDNumPGMapRegionDDetail:
			[self setComboRegionDDetail];
			break;
		case kAudUDNumPGMapRegionEDetail:
			[self setComboRegionEDetail];
			break;
		case kAudUDNumPGMapRegionFDetail:
			[self setComboRegionFDetail];
			break;
		case kAudUDNumPGMapRegionGDetail:
			[self setComboRegionGDetail];
			break;
		case kAudUDNumPGMapRegionHDetail:
			[self setComboRegionHDetail];
			break;
		case kAudUDNumPGMapRegionIDetail:
			[self setComboRegionIDetail];
			break;
			
		default:
			break;
	}
	
};
@end

